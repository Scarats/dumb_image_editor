#include "ft_printf.h"

int	type_s(char *str)
{
	size_t	i;

	if (str == NULL)
	{
		type_s("(null)");
		return (6);
	}
	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
	return (i);
}
